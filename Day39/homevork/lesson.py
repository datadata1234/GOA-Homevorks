# Multiply

def multiply(a, b):
   return a * b

# Even or Odd

def even_or_odd(number):
    if number % 2 == 0:
        return "Even"
    else:
        return "Odd"
    
# Convert a Number to a String!

def number_to_string(num):
    return str(num)

# Reversed Strings

def solution(string):
    return string[::-1]

# Return Negative

def make_negative( number ):
    if number > 0:
        return number
    else:
        return number
