# 1)კომენტარის მეშვეობით ახსენით .lower() / .upper() / .capitalize() / .find() ფუნქციების დანიშნულება

# .lower() - სტრიქონის ყველა ასოს გარდაქმნის პატარა ასოდ.

# .upper() - სტრიქონის ყველა ასოს გარდაქმნის დიდ ასოდ.

# .capitalize() - სტრიქონის პირველ ასოს გარდაქმნის დიდ ასოდ, დანარჩენს პატარად.

# .find() - ეძებს ქვესტრიქონს სტრიქონში და აბრუნებს მის პირველ ინდექსს. თუ ვერ იპოვის, აბრუნებს -1-ს.


# 2)მომხმარებელს შემოატანინეთ მისი გვარი და შეამოწმეთ, თუ გვარის პირველი ასო არის 
# დიდი ასო, მაშინ დაუბეჭდეთ "Hello",თუ არ არის მაშინ დაუბეჭდეთ "Bye"

surname = input("enter your surname")

if surname[0].isupper():
    print("Hello")
else:
    print("Bye")


# 3)მომხმარებელს შემოატანინეთ სახელი, შემდეგ შემოატანინეთ ასო და თუ ამ სახელში ეს ასო არიქნება, მაშინ გამოუტანეთ "Can't find character", თუ იქნება
#  მაშინ გამოუტანეთ "found it" და გვერდზე მიუწერეთ ამ ასოს ინდექსი

name = input("შეიყვანეთ თქვენი სახელი: ")
letter = input("შეიყვანეთ ასო: ")

index = name.find(letter)

if index == -1:
    print("Can't find character")
else:
    print("Found it, index:" )

# 4)მომხმარებელს შემოატანინეთ მისი გვარი, შემდეგ შეეკითხეთ როგორ უნდა რო შეიცვალოს მისი გვარის ასოები, 
# თუ გიპასუხებთ "upper" გადაიყვანეთ მთლიანი გვარი დიდ ასოებად, თუ გიპასუხებთ "lower" გადაიყვანეთ მთლიანი
#  გვარი პატარა ასოებად და თუ გიპასუხებთ "capitalize" გადაიყვანეთ გვარის მხოლოდ პირველი ასო დიდ ასოდ.

surname = input("შეიყვანეთ თქვენი გვარი: ")
choice = input("როგორ გსურთ გვარის შეცვლა? (upper/lower/capitalize): ")

if choice.lower() == "upper":
    print(surname.upper())
elif choice.lower() == "lower":
    print(surname.lower())
elif choice.lower() == "capitalize":
    print(surname.capitalize())
else:
    print("არასწორი არჩევანი")